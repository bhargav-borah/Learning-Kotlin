// Assisgning a top-level variable
val myName: String = "abcd"

fun main() {
    // val keyword is used for variables which aren't to be reassigned
    // var keyword is used for variables which can be reassigned
    val name1: String = "Bhargav"
    
    println(name1)
    
    var name2: String = "Bagga"
    name2 = "Bugs"
    
    println(myName)    
    
}
